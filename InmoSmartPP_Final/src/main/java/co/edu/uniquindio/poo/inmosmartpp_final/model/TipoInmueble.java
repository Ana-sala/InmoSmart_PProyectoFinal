package co.edu.uniquindio.poo.inmosmartpp_final.model;

/**
 * Enumeración que representa los diferentes tipos
 * de inmuebles disponibles dentro del sistema inmobiliario.
 *
 * Esta enumeración permite clasificar las propiedades
 * según su uso o categoría.
 *
 * Tipos disponibles:
 *
 * CASA -> Vivienda independiente destinada para habitación.
 *
 * APARTAMENTO -> Unidad residencial ubicada en un edificio.
 *
 * LOCAL -> Espacio comercial destinado para negocios.
 *
 * TERRENO -> Lote o espacio sin construcción.
 *
 * @author Maria Camila
 */
public enum TipoInmueble {

    /**
     * Representa una casa.
     */
    CASA,

    /**
     * Representa un apartamento.
     */
    APARTAMENTO,

    /**
     * Representa un local comercial.
     */
    LOCAL,

    /**
     * Representa un terreno o lote.
     */
    TERRENO
}

