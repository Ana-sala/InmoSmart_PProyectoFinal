package co.edu.uniquindio.poo.inmosmartpp_final.model;

/**
 * Enumeración que representa los tipos de operaciones
 * inmobiliarias disponibles dentro del sistema.
 *
 * Estas operaciones permiten definir el tipo de negocio
 * realizado sobre un inmueble.
 *
 * Tipos disponibles:
 *
 * VENTA -> El inmueble será vendido al comprador.
 *
 * ARRIENDO -> El inmueble será alquilado temporalmente.
 *
 * @author Maria Camila
 */
public enum TipoOperacion {

    /**
     * Operación de venta de inmueble.
     */
    VENTA,

    /**
     * Operación de arriendo de inmueble.
     */
    ARRIENDO
}
